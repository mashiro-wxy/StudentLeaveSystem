package com.stu_leave.entity;

public class Teacher {
    private String teacher_no;
    private String teacher_name;
    private String teacher_pro;
    private String teacher_sex;

    public String getTeacher_no() {
        return teacher_no;
    }

    public void setTeacher_no(String teacher_no) {
        this.teacher_no = teacher_no;
    }

    public String getTeacher_name() {
        return teacher_name;
    }

    public void setTeacher_name(String teacher_name) {
        this.teacher_name = teacher_name;
    }

    public String getTeacher_pro() {
        return teacher_pro;
    }

    public void setTeacher_pro(String teacher_pro) {
        this.teacher_pro = teacher_pro;
    }

    public String getTeacher_sex() {
        return teacher_sex;
    }

    public void setTeacher_sex(String teacher_sex) {
        this.teacher_sex = teacher_sex;
    }
}
